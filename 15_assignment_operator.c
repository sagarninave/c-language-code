#include <stdio.h>
int main()
{
    printf("\n-------------------------------\n");

    // operater(=)
    int assigns1 = 5, assigns2;
    printf(" Assigns : =  Value of assigns2 = %d\n\n", assigns2);

    // operater (+=)
    int add1 = 8, add2 = 2;
    printf(" Add AND  : +=  Value of add2 = %d\n\n", add2);

    // operater (-=)
    int subtract1 = 5, subtract2 = 15;
    printf(" Subtract AND : -=  Value of subtract2 = %d\n\n", subtract2);

    // operater (*=)
    int multiplies1 = 8, multiplies2;
    printf(" Multiply AND : *=  Value of multiplies2 = %d\n\n", multiplies2);

    // operater (/=)
    int divides1 = 2, divides2;
    printf(" Divide AND  : /=  Value of divides2 = %d\n", divides2);

    printf("\n-------------------------------\n");
}
